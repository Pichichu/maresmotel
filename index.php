        function ValidationProjectContactForm() {
            var error = true;
            $('#contact_form input[type], #contact_form textarea').each(function(index) {

                // validacion para email
                if (index === 1) {
                    if (!(/(.+)@(.+){2,}\.(.+){2,}/.test($(this).val()))) {
                        $("#contact_form").find("input:eq(" + index + ")").addClass("required-error");
                        error = false;
                    } else {
                        $("#contact_form").find("input:eq(" + index + ")").removeClass("required-error");
                    }
                } else if (index == 3) {
                    if ($(this).val() == null || $(this).val() == "") {
                        $("#contact_form [name=message]").addClass("required-error");
                        error = false;
                    } else {
                        $("#contact_form [name=message]").removeClass("required-error");
                    }
                    // validacion no este vacio
                } else {
                    if ($(this).val() == null || $(this).val() == "") {
                        $("#contact_form").find("input:eq(" + index + ")").addClass("required-error");
                        error = false;
                    } else {
                        $("#contact_form").find("input:eq(" + index + ")").removeClass("required-error");
                    }
                }

            });
            return error;
        }
